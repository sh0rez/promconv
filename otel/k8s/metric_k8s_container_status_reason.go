// Describes the number of K8s containers that are currently in a state for a given reason
type ContainerStatusReason struct {
	*prometheus.GaugeVec
	extra ContainerStatusReasonExtra
}

func NewContainerStatusReason() ContainerStatusReason {
	labels := []string{AttrContainerStatusReason("").Key()}
	return ContainerStatusReason{GaugeVec: prometheus.NewGaugeVec(prometheus.GaugeOpts{
		Name: "k8s_container_status_reason",
		Help: "Describes the number of K8s containers that are currently in a state for a given reason",
	}, labels)}
}

func (m ContainerStatusReason) With(containerStatusReason AttrContainerStatusReason, extras ...interface{}) prometheus.Gauge {
	return m.GaugeVec.WithLabelValues(containerStatusReason.Value())
}

// Deprecated: Use [ContainerStatusReason.With] instead
func (m ContainerStatusReason) WithLabelValues(lvs ...string) prometheus.Gauge {
	return m.GaugeVec.WithLabelValues(lvs...)
}

